package br.com.tiposprimitivos;

public class TipoJavas {
	public static void main(String args[]) {
		int intMaximo = Integer.MAX_VALUE ;
		int intMínimo = Integer.MIN_VALUE;
		double doubleMáxino = Double.MAX_VALUE;
		double doubleMínimo = Double.MIN_NORMAL;
		long longMáximo = Long.MAX_VALUE;
		long longMínimo = Long.MIN_VALUE;
		float floatMáximo = Float.MAX_VALUE;
		float floatMínimo = Float.MIN_VALUE;
		short shortMáximo = Short.MAX_VALUE;
		short shortMínimo = Short.MIN_VALUE;
		byte byteMáximo = Byte.MAX_VALUE;
		byte byteMínimo = Byte.MIN_VALUE;
		boolean booleanVerdadeiro = Boolean.TRUE;
		boolean booleanFalso = Boolean.FALSE; 
		
		System.out.print("LIMITE TIPOS\n");
		System.out.print("-------------------------------------------------\n");
		System.out.print("O menor numero inteiro: " +intMínimo + "\n");
		System.out.print("O maior numero inteiro: " +intMaximo + "\n");
		System.out.print("-------------------------------------------------\n");
		System.out.print("O menor numero double: " +doubleMínimo + "\n");
		System.out.print("O maior numero double: " +doubleMáxino+ "\n");
		System.out.print("-------------------------------------------------\n");
		System.out.print("O menor numero long: " +longMínimo+ "\n");
		System.out.print("O maior numero long: " +longMáximo+ "\n");
		System.out.print("-------------------------------------------------\n");
		System.out.print("O menor numero float: "+floatMínimo+ "\n");
		System.out.print("O maior numero float: "+floatMáximo+ "\n");
		System.out.print("-------------------------------------------------\n");
		System.out.print("O menor numero short: "+shortMínimo+ "\n");
		System.out.print("O maior numero short: "+shortMáximo+ "\n");
		System.out.print("-------------------------------------------------\n");
		System.out.print("O menor numero byte: "+byteMínimo+ "\n");
		System.out.print("O maior numero byte: "+byteMáximo+ "\n");
		System.out.print("-------------------------------------------------\n");
		System.out.print("O boolean verdadeiro: "+booleanVerdadeiro+ "\n");
		System.out.print("O boolean falso: "+booleanFalso+ "\n");
		
	}

}
